
DROP TABLE IF EXISTS FORMS CASCADE;
DROP TABLE IF EXISTS DIPLOMAS CASCADE;

CREATE TABLE DIPLOMAS (
DIPLOMA_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
NAME_DIPLOMA VARCHAR,
TYPE_DIPLOMA VARCHAR,
QUANTITY INTEGER DEFAULT 0
);


CREATE TABLE FORMS (
FORMS_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
NAME_FORMS VARCHAR,
QUANTITY INTEGER DEFAULT 0
);
